// Печатает текст крупными "пиксельными" буквами.
// blockW, blockH — размер одного логического "пикселя" в символах терминала
//                  (по ширине и по высоте отдельно). Символ в терминале выше,
//                  чем шире (обычно ~1:2), поэтому blockW делаем примерно
//                  вдвое больше blockH — иначе диагонали (M, N, S...) рвутся
//                  и буквы выглядят кривыми.
// letterSpacing  — зазор между буквами в символах (не масштабируется)
void printBanner(const string& text, int blockW, int blockH, int letterSpacing = 2) {
    for (int row = 0; row < 5; ++row) {
        for (int rep = 0; rep < blockH; ++rep) {
            for (char ch : text) {
                auto it = FONT.find(ch);
                const string& pattern = (it != FONT.end()) ? it->second[row] : FONT.at(' ')[row];
                for (char px : pattern) {
                    for (int i = 0; i < blockW; ++i)
                        cout << (px == '#' ? '#' : ' ');
                }
                for (int i = 0; i < letterSpacing; ++i)
                    cout << ' ';
            }
            cout << '\n';
        }
    }
}

int main() {
    cout << '\n';
    printBanner("MAIBENBEN", 3, 2);        // крупный заголовок, пропорции выровнены
    cout << '\n';
    printBanner("ONLY FOR BETTER", 2, 1);  // слоган поменьше
    cout << '\n';
    return 0;
}
